package javabasics;

public class AssignmentOperator {
    public static void main(String[] args) {
        int a = 20;
        int b = a;
        System.out.println(b);
        System.out.println(a+=20);
        System.out.println(a-=30);
        System.out.println(a/=5);
        System.out.println(a*=10);
    }
}
