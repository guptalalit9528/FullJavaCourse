package javabasics;

public class RelationOperator {
    public static void main(String[] args) {
        int i = 20;
        int j = 30;
        System.out.println("Equal to : "+(i==j));
        System.out.println("Greater than : " +(i>j));
        System.out.println("Less then : " +(i<j));
        System.out.println("grater than equal to : "+(i>=j));
        System.out.println("Less then equal to : "+(i<=j));
        System.out.println("Not equal to : "+(i!=j));
    }
}
