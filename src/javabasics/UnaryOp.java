package javabasics;

public class UnaryOp {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("This is a PreIncrement operator : " +(++a));
        System.out.println("This is a PostIncrement operator : " +(a++));
        System.out.println("This is a PreDecriment operator : " +(--a));
        System.out.println("This is a PostDecriment operator : " +(a--));

    }
}
